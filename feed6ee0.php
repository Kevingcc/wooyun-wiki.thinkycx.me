1、生产环境

----------

1.1 安全开发

应用程序和计算机系统开发的首要目的往往是满足功能需求，而非安全。为了满足这两方面的需求，在设计和开发时必须同时考虑安全性和功能性。安全应该交织在产品的核心之中，并在各个层面提供保护。相对于在产品与其他应用程序集成时再开发可能影响总体功能和留下安全漏洞的前端或者包装程序，在设计之初就考虑进安全因素的方法要好得多。…</description>
